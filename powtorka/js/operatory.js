// ++
let i = 4;
i++;
/*  
    i++
    i = i+1
    i=1+1
    i=2
*/
console.log(i);

console.log(`preInkremetacja`);
let a = 1;
console.log(a);
console.log(a++);
console.log(a);

console.log(`postInkrementacja`);
let b = 1;
console.log(b);
console.log(++b);
console.log(b);

